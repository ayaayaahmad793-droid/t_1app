class Product {
  final String name;
  final String code;
  final String price;
  final String oldPrice;
  final String image;
  final bool available;
  final String amount;

  Product({
    required this.name,
    required this.code,
    required this.price,
    required this.oldPrice,
    required this.image,
    required this.available,
    required this.amount,
  });
}
